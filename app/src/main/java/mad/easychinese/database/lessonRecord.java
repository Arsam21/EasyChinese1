package mad.easychinese.database;


public class lessonRecord {

    private int image;
    private String des;
    private String type;

    public int getImage() {
        return image;
    }
    public void setImage(int image){
        this.image = image;
    }

    public String getDes() {
        return des;
    }
    public void setDes(String des) {
        this.des = des;
    }



    public String gettype() {
        return type;
    }
    public void settype(String type) {
        this.type = type;
    }

}
